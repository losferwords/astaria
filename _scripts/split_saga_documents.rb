def read_note(path)
  text = File.read(path)
  match = text.match(/\A---\n(.*?)\n---\n/m)
  frontmatter = match ? YAML.safe_load(match[1], aliases: true) : {}
  body = match ? text[match.end(0)..] : text
  [frontmatter || {}, body]
end

def yaml_scalar(value)
  value.to_s.inspect
end

def yaml_array(lines, key, values)
  values = values.compact.uniq
  return if values.empty?

  lines << "#{key}:"
  values.each { |value| lines << "  - #{yaml_scalar(value)}" }
end

def write_note(path, frontmatter, title, body)
  FileUtils.mkdir_p(File.dirname(path))
  lines = ["---"]
  frontmatter.each do |key, value|
    if value.is_a?(Array)
      yaml_array(lines, key, value)
    elsif value == true || value == false || value.is_a?(Integer)
      lines << "#{key}: #{value}"
    else
      lines << "#{key}: #{yaml_scalar(value)}"
    end
  end
  lines << "---"

  File.write(path, "#{lines.join("\n")}\n\n# #{title}\n\n#{body.to_s.strip}\n")
end

def chapters_dataview(folder)
  <<~MARKDOWN
    ```dataview
    TABLE chapter AS "Глава", year AS "Год", season AS "Сезон", region AS "Регион"
    FROM "#{folder}"
    WHERE contains(["chapter", "session"], type)
    SORT chapter ASC
    ```
  MARKDOWN
end

def strip_links_section(text)
  text.sub(/\n+## Связи\n\n```dataview\n.*?\n```\s*\z/m, "").strip
end

def chapter_number(label)
  return 0 if label.start_with?("Пролог")
  return 9 if label.start_with?("Эпилог")

  label[/Глава\s+(\d+)/, 1].to_i
end

def chapter_filename(label)
  number = chapter_number(label)
  if label == "Пролог"
    return "Глава 000 - Пролог.md"
  elsif label.start_with?("Пролог - ")
    return "Глава 000 - #{label.sub(/\AПролог - /, "")} (Пролог).md"
  elsif label.start_with?("Эпилог - ")
    subtitle = label.sub(/\AЭпилог - /, "")
    subtitle = subtitle.split.map.with_index { |word, index| index.zero? ? word : word.downcase }.join(" ")
    return "Глава %03d - #{subtitle} (Эпилог).md" % number
  end

  subtitle = label.sub(/\AГлава\s+\d+\s*-?\s*/, "").strip
  suffix = subtitle.empty? ? "" : " - #{subtitle}"
  "Глава %03d#{suffix}.md" % number
end

def split_sections(text)
  matches = text.to_enum(:scan, /^\[## ([^\]]+)\]\(#\d+\)\s*$/).map do
    [Regexp.last_match.begin(0), Regexp.last_match.end(0), Regexp.last_match(1)]
  end
  return [text.strip, []] if matches.empty?

  intro = text[0...matches.first[0]].strip
  sections = matches.each_with_index.map do |(start_pos, end_pos, label), index|
    next_start = matches[index + 1]&.first || text.length
    [label, text[end_pos...next_start].strip]
  end
  [intro, sections]
end

def write_saga_overview(name, config, source_frontmatter, intro)
  overview_path = File.join(config[:dir], "#{name}.md")
  frontmatter = {
    "title" => name,
    "type" => name == "Ветер Перемен" ? "campaign" : "document",
    "category" => "Литература",
    "status" => source_frontmatter["status"] || "imported",
    "publish" => false,
    "draft" => !!source_frontmatter["draft"],
    "wip" => !!source_frontmatter["wip"],
    "wa_id" => source_frontmatter["wa_id"],
    "wa_url" => source_frontmatter["wa_url"],
    "wa_slug" => source_frontmatter["wa_slug"],
    "cover_image" => source_frontmatter["cover_image"],
    "tags" => ["astaria", config[:campaign_tag]]
  }.compact
  body = []
  body << "!#{source_frontmatter["cover_image"]}\n" if source_frontmatter["cover_image"].to_s.start_with?("[[")
  cleaned_intro = strip_links_section(intro)
    .sub(/\A!\[\[[^\]]+\]\]\n+/m, "")
    .sub(/\A## Основной текст\n+/m, "")
  body << cleaned_intro
  body << "\n## Главы\n\n#{chapters_dataview(config[:dir])}"
  write_note(overview_path, frontmatter, name, body.join("\n\n"))
end

def write_split_chapters(name, config, source_frontmatter, sections)
  sections.each do |label, content|
    number = chapter_number(label)
    path = File.join(config[:dir], chapter_filename(label))
    if config[:existing_chapters_are_canonical]
      prefix = "Глава %03d" % number
      next if Dir[File.join(config[:dir], "#{prefix}*.md")].any?
    end

    title = label
    title = "Глава %03d" % number if label == "Глава #{number}"
    title = "Глава 000 - Пролог" if label == "Пролог"
    title = "Глава 000 - #{label.sub(/\AПролог - /, "")} (Пролог)" if label.start_with?("Пролог - ")
    if label.start_with?("Эпилог - ")
      subtitle = label.sub(/\AЭпилог - /, "")
      subtitle = subtitle.split.map.with_index { |word, index| index.zero? ? word : word.downcase }.join(" ")
      title = "Глава %03d - #{subtitle} (Эпилог)" % number
    end
    frontmatter = {
      "title" => title,
      "type" => config[:type],
      "saga" => name,
      "campaign" => name == "Ветер Перемен" ? name : nil,
      "chapter" => number,
      "status" => "imported",
      "source" => "WorldAnvil",
      "wa_parent_id" => source_frontmatter["wa_id"],
      "publish" => false,
      "tags" => ["astaria", config[:campaign_tag]]
    }.compact
    write_note(path, frontmatter, title, strip_links_section(content))
  end
end

def split_imported_saga(name, config)
  return unless File.exist?(config[:source])

  frontmatter, body = read_note(config[:source])
  text = strip_links_section(body)
  text = text.sub(/\A# .+?\n+/m, "")
  intro, sections = split_sections(text)
  write_saga_overview(name, config, frontmatter, intro)
  write_split_chapters(name, config, frontmatter, sections)
end

